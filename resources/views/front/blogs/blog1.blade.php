            <ul>
              <li>
                In some cases, particularly for high-level hires, multiple people may need to interview a candidate at one time. Assembling a diverse panel of the most appropriate interviewers — especially when your company’s workforce is dispersed — may be logistically easier to accomplish virtually, which can, in turn, yield better results.
              </li>
            </ul>
            <li class="list-item">Reduced ghosting.</li>
            <ul>
