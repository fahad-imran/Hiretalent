                When unemployment rates remained low and the labour market was highly competitive, ghosting was a big problem. 83% of companies experienced no-shows for scheduled interviews or new hires who failed to appear for their first day. However, virtual hiring technology can help reduce ghosting by automating and streamlining initial communications (such as RSVPs) between applicants and hiring managers and recruiters. And because virtual hiring helps save candidates’ time and effort, talent professionals have reported a reduction in ghosting.
